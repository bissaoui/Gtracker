    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed

        String marque = MarqueTxt.getText();
        String Matricule = MatriculeTxt.getText();
        String Type = TypeTxt.getText();
        
        if(vs.create(new Vehicule(marque, Matricule, Type))){
            JOptionPane.showMessageDialog(this, " Vehicule "+ Matricule + " bien enregistré");
        }
        loadVehicule();
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
         String marque = MarqueTxt.getText();
        String Matricule = MatriculeTxt.getText();
        String Type = TypeTxt.getText();
        Vehicule v ;
        v = vs.findById(id);
        v.setMatricule(Matricule);
        v.setMarque(marque);
        v.setType(Type);
        
       if( vs.update(v)){
            JOptionPane.showMessageDialog(this, " Vehicule "+ Matricule + " bien Modifié");

       }
                loadVehicule();

        
    }//GEN-LAST:event_btnModifierActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        // TODO add your handling code here:
        int reponse = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer cette position");
        
        if (reponse == 0 && id != 0) {
            if(vs.delete(vs.findById(id))){
                JOptionPane.showMessageDialog(this, " Vehicule Supprimé");
            }
        }
        loadVehicule();

    }//GEN-LAST:event_btnSupprimerActionPerformed

