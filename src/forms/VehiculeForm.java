    public void loadVehicule(){
        model.setRowCount(0);
        for (Vehicule v: vs.findAll()) {
            model.addRow(new Object[]{
                v.getId(),
                v.getMatricule(),
                v.getMarque(),
                v.getType(),
            });
        }
    }
